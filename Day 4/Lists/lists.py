#list is a grouped piece of data in one variable
states_of_america = ["Delaware", "Pennsylvania", "New Jersey"]

#lists have order, determined by their order in the list
#can pull out a piece from a list by using [] in print statement
print(states_of_america[0])
#if you have a negative number in the '[]' it will count from the end of the list
print(states_of_america[-1])

#can edit items in the list
states_of_america[1] = "Pencilvania"

#to add an item to the end of the list
states_of_america.append("Angelaland")

print(states_of_america)

